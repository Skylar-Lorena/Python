# Looping
# A loop is a way to execute some piece of code over and over again.
# A for loop is used when you want to repeat something a number of times. 
numbers = [1, 2, 3, 4, 5]
for number in numbers:
    print(number)

#Range()
# A range is a way to get a list of numbers from start to end
numbers = range(1, 10)
print(numbers)
for number in range(1,7):
    print(number)
# The range function takes up two parameters: start and end

#While
# A while loop is a way to execute some piece of code while a condition is true.
students = 33
while students >0:
    print (students)
    students -= 1

    